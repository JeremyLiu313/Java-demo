package as2;

public class book {
    private String ISBN, title, author, category;
    private int totalCopies, availableCopies;
    
    // The constructor of the 'book'
    public book(String ISBN, String title, String author, String category, int totalCopies, int availableCopies) {
        this.ISBN = ISBN;
        this.title = title;
        this.author = author;
        this.category = category;
        this.totalCopies = totalCopies;
        this.availableCopies = availableCopies;
    }
    // Getter and Setter
    public String getISBN() {
        return ISBN;
    }

    public void setISBN(String ISBN) {
        this.ISBN = ISBN;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }


    public int getTotalCopies() {
        return totalCopies;
    }

    public void setTotalCopies(int totalCopies) {
        this.totalCopies = totalCopies;
    }

    public int getAvailableCopies() {
        return availableCopies;
    }

    public void setAvailableCopies(int availableCopies) {
        this.availableCopies = availableCopies;
    }

    public String toString() {
        return ISBN + ";" + title + ";" + author + ";" + category + ";" + totalCopies + ";" + availableCopies;
    }
}
